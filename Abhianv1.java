public class Abhianv1 {
    public static void printArr(int arr[]) {
        for (int i = 0; i < arr.length; i++) {
            System.out.print(arr[i] + "  ");
        }
    }

    public static void main(String[] args) {
        int arr[] = { 1, 2, 3, 4, 34 };
        printArr(arr);
    }
}
